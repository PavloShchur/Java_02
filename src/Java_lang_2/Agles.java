package Java_lang_2;

public class Agles {

	public static void main(String[] args) {
		double theta = 120.0;
		System.out.println(theta + " градусів рівно " + Math.toRadians(theta) + " радіан.");

		theta = 1.312;
		System.out.println(theta + " радіан рівно " + Math.toDegrees(theta) + " градусів.");

	}

}
